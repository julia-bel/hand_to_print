class Coords {
  double height = 100;
  double width = 100;
  double top = 0;
  double left = 0;

  void setTop(double value) {
    top = value;
  }

  void setLeft(double value) {
    left = value;
  }

  void setWidth(double value) {
    width = value;
  }

  void setHeight(double value) {
    height = value;
  }
}